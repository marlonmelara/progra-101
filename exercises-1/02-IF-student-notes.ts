// STATEMENT: Programa para evaluar el rendimiento académico de un estudiante

// Calificación del estudiante (0-100)
let grade: number = 30;

// Evaluación del rendimiento según la calificación
if (grade >= 60) {
  console.log("El estudiante aprobó el curso");
} else if (grade >= 50) {
  console.log("Estudia más");
} else {
  console.log("El estudiante NO aprueba el curso");
}
