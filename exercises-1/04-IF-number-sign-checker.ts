// STATEMENT: Programa para clasificar un número según su signo (positivo, negativo o cero)

// Número a evaluar
let number: number = 5;

// Clasificación del número según su signo
if (number > 0) {
  console.log("El número es positivo.");
} else if (number === 0) {
  console.log("El número es cero");
} else {
  console.log("El número es negativo.");
}
