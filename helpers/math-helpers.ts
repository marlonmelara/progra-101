export function sumTwoNumbers(a: number, b: number) {
  return a + b;
}

export function subtractTwoNumbers(a: number, b: number) {
  return a - b;
}

export function timesTwoNumbers(a: number, b: number) {
  return a * b;
}

export function divideTwoNumbers(a: number, b: number) {
  return a / b;
}

export function moduleTwoNumbers(a: number, b: number) {
  return a % b;
}
